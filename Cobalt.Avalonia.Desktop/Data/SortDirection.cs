namespace Cobalt.Avalonia.Desktop.Data;

/// <summary>
/// Specifies the direction in which items are sorted within a <see cref="CollectionView"/>.
/// </summary>
public enum SortDirection
{
    /// <summary>Items are sorted in ascending order (smallest to largest, A to Z).</summary>
    Ascending,

    /// <summary>Items are sorted in descending order (largest to smallest, Z to A).</summary>
    Descending,
}
